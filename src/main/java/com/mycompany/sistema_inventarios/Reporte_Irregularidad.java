    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String codigoProducto = txtcodigoproducto.getText();

        if (codigoProducto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un cÃ³digo de producto.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventarios", "root", "")) {
            String query = "SELECT producto, stock FROM almacen1 WHERE codigoproducto = ? AND stock >= 0";

            try (PreparedStatement statement = connection.prepareStatement(query)) {
                statement.setString(1, codigoProducto);

                try (ResultSet resultSet = statement.executeQuery()) {
                    if (resultSet.next()) {
                        txtproducto.setText(resultSet.getString("producto"));
                        txtcantidad.setText(String.valueOf(resultSet.getInt("stock")));
                    } else {
                        JOptionPane.showMessageDialog(this, "Producto no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

